    private String[] getFarmNames(){
    	if(ServerData.farms.isEmpty())
    		return new String[] {"You have no farm's"};
        String[] tmp = new String[ServerData.farms.size()];
        for (int i = 0; i < tmp.length; i++) {
			tmp[i] = ServerData.farms.get(i).getName();
		}
        return tmp;
    }

